const sessions = []

const debugSession= (session) => {
    console.log(JSON.stringify(session, null, 2))
}
const getSession = (id) =>{
    return sessions.filter(s => s.sessionId == id)[0]
}
const constructSystemMessage=(template, context) => {
    return template.replace("{{context}}", context)
}
const initSession= (templateId, context, userId) => {
    var template = templates[templateId]
    var convo = [{role: "system", content: constructSystemMessage(template.invocation, context)}]
    template.examples.forEach(example => convo.push(example))
    var session = {
        userId: userId, 
        sessionId: uuid.v4(), 
        templateId: templateId, 
        convo:convo, 
        tokensUsed: 0, 
        model: template.model, 
        temperature: template.temperature, 
        max_tokens: template.max_tokens,
        currentContext: context
    }
    debugSession(session)
    sessions.push(session)
    return session
}

const gpt = async(session, query, sender, overrides)=>{
    session.convo.push({role: sender, content: query})

    try {
        const completion = await openai.createChatCompletion({
        model: session.model,
        temperature: session.temperature,
        max_tokens: session.max_tokens,
        n: 1,
        messages: session.convo,
        });

        console.log(completion.data.choices[0].message);
        session.convo.push(completion.data.choices[0].message)
        return completion.data.choices[0].message
    
    } catch(openAiFuckupException) {
        console.log("fatal error (have you paid your openai bill?")
        console.log("please retry your request")
        return null
    }

}

